        Items::Items()
        {
            for (size_t i = 0; i < ItemsDb.size(); ++i)
                map[ItemsDb[i].name] = i;
        }

        Items::Item Items::get(size_t id) const
        {
            if (id >= ItemsDb.size())
                return{};

            return ItemsDb[id];
        }

        Items::Item Items::get(const std::string &name) const
        {
            const auto it = map.find(name);

            if (it == std::end(map))
                return{};

            const auto id = it->second;
            return ItemsDb[id];
        }

        std::vector<std::string> Items::getNames() const
        {
            std::vector<std::string> ret;

            for (const auto &item : ItemsDb)
                ret.emplace_back(item.name);

            return ret;
        }

